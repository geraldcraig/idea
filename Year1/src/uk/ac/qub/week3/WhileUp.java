public class WhileUp {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		int count;
		count = 1;
		
		while (count < 11) {
			System.out.println("Count is " + count);
			count ++;
		}
		System.out.println("end");
	}

}
